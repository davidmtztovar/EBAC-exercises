package com.ebac.variables;

public class VariablesPrimitivas {
	public static void main(String[] args) {
      int number=7;
      short shortNumber= 32767;
      byte byteValue= 127;
      long longNumber= 35;
      double doubleNumber=27.15484544;
      float floatNumber= 10.5f;
      boolean booleanValue= true;
      char charValue= 'a';
      
      System.out.println("Primitivos \nint: "+number+"\nbyte: "+byteValue
    		             +"\nshort: "+shortNumber+"\nlong: "+longNumber
    		             +"\ndouble: "+doubleNumber+"\nfloat: "+floatNumber
    		             +"\nboolean:"+booleanValue+"\nchar: "+charValue);
	}
}
