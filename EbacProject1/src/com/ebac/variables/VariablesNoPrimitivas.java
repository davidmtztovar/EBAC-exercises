package com.ebac.variables;

public class VariablesNoPrimitivas {
	public static void main(String[] args) {
		Byte byteValue = 127;
		Short shortValue = 4451;
		Integer intValue = 95;
		Long longValue = 878744L;
		Float floatValue = 96.1848F;
		Double doubleValue = 78.6254545;
		Character characterValue = 'b';
		Boolean booleanValue = false;

		System.out.println("No Primitivos \nInteger: " + intValue + "\nByte: " + byteValue + "\nShort: " + shortValue
				+ "\nLong: " + longValue + "\nDouble: " + doubleValue + "\nFloat: " + floatValue + "\nBoolean:"
				+ booleanValue + "\nCharacter: " + characterValue);
	}

}
